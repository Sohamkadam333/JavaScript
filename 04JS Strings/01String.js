// string literal
let str1 = 'Soham';

// string template 
let str2 = `Hello ${str1}`;

console.log(str1);
console.log(str2);

// Escaping Special character
let str3 = 'I\'m a string!';
console.log(str3);

// string length
console.log(str2.length);


// accessing character
console.log(str2[0]);


// concating string using + operator
let name = 'soham';
let lName = name + ' kadam';
console.log(lName);


// converting values to string
let numStr = 10;
console.log(numStr.toString());


// comparing strings
console.log('a' > 'b');
console.log('a' < 'b');