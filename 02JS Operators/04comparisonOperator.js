// A comparison operator returns a Boolean value indicating that the comparison is true or not.

console.log(20 > 10);
console.log(20 < 10);
console.log(10 <= 10);
console.log(20 >= 10);
console.log(20 == 10);
console.log(10 != 10);

// compare strings
let str1 = 'alice';
let str2 = 'bob';
console.log(str1.length > str2.length);

console.log(10 == '10'); // true
console.log(10 === '10'); // false


